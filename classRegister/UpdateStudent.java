    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if(
            !fNameFld.getText().equals("") &&
            !lNameFld.getText().equals("")             
          ){
            final String UPDATE_STATEMENT = "UPDATE STUDENT SET FIRSTNAME=?, LASTNAME=?, GENDER=?, PROGRAMID=? WHERE STUDENTID=?";
            
            try(
               Connection connection = DriverManager.getConnection(DATABASE_URL);
               PreparedStatement preparedStatement = connection.prepareStatement(UPDATE_STATEMENT);
               ){
                
                String genderText = (String)genderList.getSelectedItem();
                
                
                
                preparedStatement.setString(1, fNameFld.getText().toUpperCase());
                preparedStatement.setString(2, lNameFld.getText().toUpperCase());
                preparedStatement.setString(3, genderText.toUpperCase());
                preparedStatement.setInt(4, idFromProgram);
                preparedStatement.setInt(5, studentId);
                
                
                preparedStatement.executeUpdate();
                
                JOptionPane.showMessageDialog(this,"Student Update","Edit Form", JOptionPane.INFORMATION_MESSAGE);
            }
            catch(SQLException e){
                JOptionPane.showMessageDialog(this,"DATABASE ERROR","Edit Form", JOptionPane.ERROR_MESSAGE);
                e.printStackTrace();
            }
        }
        
                fNameFld.setText("");
                lNameFld.setText("");
                genderList.setSelectedItem("");
                programsList.setSelectedItem("");
    }//GEN-LAST:event_jButton2ActionPerformed

    private void insertUpdate(){
        
        
        if(!studentList.equals("Select Student")){
            final String SELECT_QUERY = 
            "SELECT STUDENTID, FIRSTNAME,LASTNAME,GENDER,PROGRAM FROM STUDENT JOIN PROGRAMS ON STUDENT.PROGRAMID=PROGRAMS.PROGRAMID WHERE FIRSTNAME=? AND LASTNAME=?";
            
            ResultSet resultSet;
            try(
                    Connection connection = DriverManager.getConnection(DATABASE_URL);
                    PreparedStatement preparedStatement = connection.prepareStatement(SELECT_QUERY);
               ){
                
                
                // used to insert the missing values for the sql (?) 
                //the setString method takes 2 arguments an int and the value with a matching dataType
                
                preparedStatement.setString(1,stringOne);
                preparedStatement.setString(2,stringTwo);
                
                // with this line of code we run the query
                resultSet = preparedStatement.executeQuery();
                
                if(resultSet.next()){
                   studentId = resultSet.getInt("STUDENTID");
                    
                    //here we write a code that will fill up the provided textFields in the GUI with the 
                    // right SQL data
                    fNameFld.setText(resultSet.getString("FIRSTNAME"));
                    lNameFld.setText(resultSet.getString("LASTNAME"));
                    programsList.addItem(resultSet.getString("PROGRAM"));
                    genderList.addItem(resultSet.getString("GENDER"));
          
                    
                    // HERE WE DISPLAY A POPUP BOX GIVING US UPDATE IF THE SEARCHED FIRSTNAME AND LASTNAME WAS FOUND
                    //JOptionPane.showMessageDialog(this, "Author Found", "Edit form", JOptionPane.INFORMATION_MESSAGE);
                }
                
                
                
            }catch(SQLException e){
                //here, if there seems to be any error, this will come up
                JOptionPane.showMessageDialog(this, "Database Error", "Edit form", JOptionPane.ERROR_MESSAGE);
            }
            
           
        }
        
        
    }
    
    
