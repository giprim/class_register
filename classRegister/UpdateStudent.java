     void getProgramId(){
        final String sql = "SELECT * FROM PROGRAMS WHERE PROGRAM=?";
       
       try(
               Connection connection = DriverManager.getConnection(DATABASE_URL);
               PreparedStatement preparedStatement = connection.prepareStatement(sql);
            ){
            String programText = (String)programsList.getSelectedItem();
            
            preparedStatement.setString(1,programText.toUpperCase());
            
            ResultSet resul = preparedStatement.executeQuery();
            
           
             if(resul.next())
                  idFromProgram = resul.getInt("PROGRAMID");
                 
            
                     
        }catch(SQLException e){
            e.printStackTrace();
        }
     
      
    }
     
     
