    // On the click of this button, makes sure a name was selected in the comboBox, then in the database
    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
       if(
            studentCombo.getSelectedIndex() >= 1
          ){
            final String UPDATE_STATEMENT = "DELETE FROM STUDENT WHERE FIRSTNAME=? AND LASTNAME=?";
            
            try(
               Connection connection = DriverManager.getConnection(DATABASE_URL);
               PreparedStatement preparedStatement = connection.prepareStatement(UPDATE_STATEMENT);
               ){
                
                preparedStatement.setString(1, stringOne.toUpperCase());
                preparedStatement.setString(2, stringTwo.toUpperCase());
                
                preparedStatement.executeUpdate();
                
                JOptionPane.showMessageDialog(this,"Student deleted","Edit Form", JOptionPane.INFORMATION_MESSAGE);
            }
            catch(SQLException e){
                JOptionPane.showMessageDialog(this,"DATABASE ERROR","Edit Form", JOptionPane.ERROR_MESSAGE);
                e.printStackTrace();
            }
        }
            
                studentCombo.setSelectedItem(0);
    }//GEN-LAST:event_jButton2ActionPerformed

