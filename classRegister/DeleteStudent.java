    //fetches the existing student names in the database and adding them to the comboBox
    private void updateCombo(){
        final String sql = "SELECT * FROM STUDENT";
        
        try{
            connect = DriverManager.getConnection(DATABASE_URL);
            prep = connect.createStatement();
            rs = prep.executeQuery(sql);
            
            while(rs.next()){
                studentCombo.addItem(rs.getString("FIRSTNAME") + " " + rs.getString("LASTNAME"));
            }
                     
        }catch(SQLException e){
            e.printStackTrace();
        }
        finally{
            try{
                prep.close();
                rs.close();
                connect.close();
            }catch(SQLException e){
                e.printStackTrace();
            }
        }
    }//end of UpdateCombo
    
    
